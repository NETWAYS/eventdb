<?php

abstract class BaseEventDbEventComment extends Doctrine_Record {
    
	public function setTableDefinition() {
        $this->setTableName('event_db_event_comment');

        $this->hasColumn('event_id', 'integer', 4, array(
             'type' => 'integer',
             'length' => 4,
             'fixed' => false,
             'unsigned' => false,
             'primary' => true,
             'autoincrement' => false,
             ));
        $this->hasColumn('comment_id', 'integer', 4, array(
             'type' => 'integer',
             'length' => 4,
             'fixed' => false,
             'unsigned' => false,
             'primary' => true,
             'autoincrement' => false,
             ));
    }

}